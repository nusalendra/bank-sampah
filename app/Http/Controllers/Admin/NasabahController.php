     */
    public function index()
    {
        try {
            $data = Nasabah::join('users', 'nasabahs.user_id', '=', 'users.id')
                ->where('users.role', '=', 'Nasabah')
                ->select('nasabahs.id', 'users.nomor_telephone', 'nasabahs.status')
                ->get();

            return response()->json([
                'status' => true,
                'message' => 'Data Berhasil Didapat',
